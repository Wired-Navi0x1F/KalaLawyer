-- 1. Create the 'enquiries' table (Contact Form Leads)
CREATE TABLE IF NOT EXISTS public.enquiries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    company TEXT DEFAULT 'N/A',
    email TEXT NOT NULL,
    phone TEXT NOT NULL,
    referred_by TEXT DEFAULT 'Direct',
    practice_area TEXT DEFAULT 'General Inquiry',
    message TEXT NOT NULL,
    status TEXT DEFAULT 'New',
    timestamp TIMESTAMPTZ DEFAULT NOW()
);

-- Enable Row Level Security (RLS) on enquiries table
ALTER TABLE public.enquiries ENABLE ROW LEVEL SECURITY;

-- Policy: Allow public (anonymous) to submit contact form (Insert)
DROP POLICY IF EXISTS "Allow public submissions" ON public.enquiries;
CREATE POLICY "Allow public submissions" 
ON public.enquiries 
FOR INSERT 
WITH CHECK (true);

-- Policy: Allow only authenticated admins to view/delete enquiries (Select, Delete)
DROP POLICY IF EXISTS "Allow admin read access" ON public.enquiries;
CREATE POLICY "Allow admin read access" 
ON public.enquiries 
FOR SELECT 
TO authenticated 
USING (true);

DROP POLICY IF EXISTS "Allow admin update access" ON public.enquiries;
CREATE POLICY "Allow admin update access" 
ON public.enquiries 
FOR UPDATE 
TO authenticated 
USING (true)
WITH CHECK (true);

DROP POLICY IF EXISTS "Allow admin delete access" ON public.enquiries;
CREATE POLICY "Allow admin delete access" 
ON public.enquiries 
FOR DELETE 
TO authenticated 
USING (true);


