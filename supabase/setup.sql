CREATE OR REPLACE FUNCTION public.touch_diary_record_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_touch_diary_record_updated_at ON public.diary_records;
CREATE TRIGGER trigger_touch_diary_record_updated_at
BEFORE UPDATE ON public.diary_records
FOR EACH ROW
EXECUTE FUNCTION public.touch_diary_record_updated_at();

CREATE OR REPLACE FUNCTION public.prefill_next_diary_record()
RETURNS TRIGGER AS $$
BEGIN
    IF TG_OP = 'DELETE' THEN
        IF OLD.next_date IS NOT NULL THEN
            DELETE FROM public.diary_records 
            WHERE lower(case_number) = lower(OLD.case_number) 
              AND lower(court_name) = lower(OLD.court_name)
              AND date = OLD.next_date
              AND previous_case_date = OLD.date
              AND stage_history = '[]'::jsonb
              AND next_date IS NULL;
        END IF;
        RETURN OLD;
    END IF;

    IF (TG_OP = 'INSERT' AND NEW.next_date IS NOT NULL) OR 
       (TG_OP = 'UPDATE' AND NEW.next_date IS NOT NULL AND (OLD.next_date IS NULL OR NEW.next_date <> OLD.next_date)) THEN
        INSERT INTO public.diary_records (
            date,
            court_name,
            previous_case_date,
            case_number,
            case_title,
            stage,
            client_name,
            advocates,
            judge_name,
            hearing_mode,
            recurrence,
            notes,
            stage_history,
            next_date
        ) VALUES (
            NEW.next_date,
            NEW.court_name,
            NEW.date,
            NEW.case_number,
            NEW.case_title,
            NEW.stage,
            NEW.client_name,
            NEW.advocates,
            NEW.judge_name,
            NEW.hearing_mode,
            NEW.recurrence,
            NEW.notes,
            '[]'::jsonb,
            NULL
        )
        ON CONFLICT DO NOTHING;
    END IF;

    IF (TG_OP = 'UPDATE' AND OLD.next_date IS NOT NULL AND (NEW.next_date IS NULL OR NEW.next_date <> OLD.next_date)) THEN
        DELETE FROM public.diary_records 
        WHERE lower(case_number) = lower(OLD.case_number) 
          AND lower(court_name) = lower(OLD.court_name)
          AND date = OLD.next_date
          AND previous_case_date = OLD.date
          AND stage_history = '[]'::jsonb
          AND next_date IS NULL;
    END IF;

    IF (TG_OP = 'UPDATE' AND (NEW.case_title <> OLD.case_title OR NEW.case_number <> OLD.case_number OR NEW.court_name <> OLD.court_name OR NEW.date <> OLD.date OR NEW.client_name IS DISTINCT FROM OLD.client_name OR NEW.advocates IS DISTINCT FROM OLD.advocates OR NEW.judge_name IS DISTINCT FROM OLD.judge_name OR NEW.hearing_mode IS DISTINCT FROM OLD.hearing_mode OR NEW.recurrence IS DISTINCT FROM OLD.recurrence OR NEW.notes IS DISTINCT FROM OLD.notes)) THEN
        UPDATE public.diary_records
        SET case_title = NEW.case_title,
            case_number = NEW.case_number,
            court_name = NEW.court_name,
            previous_case_date = NEW.date,
            client_name = NEW.client_name,
            advocates = NEW.advocates,
            judge_name = NEW.judge_name,
            hearing_mode = NEW.hearing_mode,
            recurrence = NEW.recurrence,
            notes = NEW.notes
        WHERE lower(case_number) = lower(OLD.case_number)
          AND lower(court_name) = lower(OLD.court_name)
          AND previous_case_date = OLD.date
          AND stage_history = '[]'::jsonb;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_prefill_next_diary_record ON public.diary_records;
CREATE TRIGGER trigger_prefill_next_diary_record
AFTER INSERT OR UPDATE OR DELETE ON public.diary_records
FOR EACH ROW
EXECUTE FUNCTION public.prefill_next_diary_record();


